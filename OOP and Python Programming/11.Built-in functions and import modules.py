# Define the function double_it directly in this script
def double_it(num):
    return num * 2

# Finding the highest and smallest values in a list
highest = max(34, 5, 1, 6, 88, 98, 4)
print(f"Highest: {highest}")

smallest = min(34, 5, 1, 6, 88, 98, 4)
print(f"Smallest: {smallest}")

# Finding the length of a list
count = len([45, 97])
print(f"Count: {count}")

# Summing the elements in a list
total = sum([34, 5, 1, 6, 88, 98, 4])
print(f"Total: {total}")

# Calling the function directly
result = double_it(45)
print(f"Result from double_it: {result}")
