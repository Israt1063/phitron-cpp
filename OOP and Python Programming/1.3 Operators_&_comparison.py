# Arithmetic operations
number1 = 9
number2 = 2

# Basic arithmetic
print("Addition:", number1 + number2)
print("Subtraction:", number1 - number2)
print("Multiplication:", number1 * number2)
print("Division:", number1 / number2)  # Regular division (returns float)
print("Modulus (Remainder):", number1 % number2)  # Remainder of division
print("Floor Division:", number1 // number2)  # Integer division (truncated result)

# Comparison operators (Python does not support ++ or -- like C++)
# = , != , > , < , <= , >=

# Exponentiation (Power)
power_result = number1 ** number2
print("Exponentiation (Power):", power_result)
